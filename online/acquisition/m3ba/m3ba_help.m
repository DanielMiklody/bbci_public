% 	* SYNTAX:
% 	* - Terminate (single-/multichar) commands with CR
% 	* - Format of all Commands is ASCII
% 	* - Exception: Register Adresses (for 'E'/'W') are hex/oct/dec
% 	* - Commands are not case sensitive; spaces are not allowed
% 	*
% 	* COMMANDS:
% 	* -----------------------------------------------------------------------------------------
% 	* SINGLE CHAR COMMANDS
% 	* -----------------------------------------------------------------------------------------
% 	* -- 'H' -------------------------- HELP
% 	* -- 'Y' -------------------------- GET FIRMWARE VERSION
% 	* -- 'D' -------------------------- DISCONNECT
% 	* -- 'G' -------------------------- GET CONFIGURATION
% 	* -- 'T' -------------------------- GET TEMPERATURE
% 	* -- 'V' -------------------------- GET BATTERY VOLTAGE
% 	* -- 'R' -------------------------- TOGGLE TRIGGER
% 	* -- 'P' -------------------------- STOP MEASUREMENT
% 	* -- 'S' -------------------------- START MEASUREMENT
% 	* -- 'U' -------------------------- UNIT TEST FOR BT PACKET RELIABILITY
% 	* -- 'I' -------------------------- MEASURES IMPEDANCE OF EEG CHANNELS
% 	* -- '#' -------------------------- GET CUSTOM INFO
% 	* -----------------------------------------------------------------------------------------
% 	* BIAS CONFIGURATION
% 	* -----------------------------------------------------------------------------------------
% 	* -- 'B' ~ 'E' -------------------- BIAS ELECTRODE - ENABLE
% 	* ----|- ~ 'D' -------------------- BIAS ELECTRODE - DISABLE
% 	* ----|- ~ 'C' -'P<xxxx>N<xxxx>' -- BIAS CONFIG INPUTS: POS/NEG CHANNELS <x>:<1-4>
% 	* -----------------------------------------------------------------------------------------
% 	* DEVICE CONFIGURATION
% 	* -----------------------------------------------------------------------------------------
% 	* -- 'C' ~ 'N' ~ 'A<xxxx>' -------- ACTIVATE NIRS CHANNELS <1-4>
% 	* ----|-----|- ~ 'L<xx>' ---------- SET NIRS LED INTENSITY <5-12> *10mA
% 	* ----|-----|- ~ 'C' ~ 'G<xx>' ---- SET NIRS CHANNEL GAIN <1/2/4/6/8/12/24>
% 	* ----|-----|-----|- ~ 'I<x>' ----- SET NIRS INPUT CHANNEL <0-5>
% 	* ----|-----|-------------------------- <0> Photo Diode
% 	* ----|-----|-------------------------- <1> GND (short)
% 	* ----|-----|-------------------------- <2> BIAS signal
% 	* ----|-----|-------------------------- <3> MVDD (supply)
% 	* ----|-----|-------------------------- <4> TEMP sensor
% 	* ----|-----|-------------------------- <5> Test signal
% 	* ----|-----|- ~ 'E' ~ 'E' --------- EVALUATION MODE ENABLE
% 	* ----|-----|-----|- ~ 'D' --------- EVALUATION MODE DISABLE
% 	* ----|-----|-----|- ~ 'R' --------- TOGGLE RAW EVAL MODE 
% 	* ----|-----|-----|- ~ 'C<x>' -------SET LED / WAVELENGTH / DARK
% 	* ----|-----|-------------------------- <0> LEDs off (Dark Measure)
% 	* ----|-----|-------------------------- <1> LED 1 on, 750nm
% 	* ----|-----|-------------------------- <2> LED 1 on, 850nm
% 	* ----|-----|-------------------------- <3> LED 2 on, 750nm
% 	* ----|-----|-------------------------- <4> LED 2 on 850nm
% 	* ----|------------------------------------------------------------------------------------
% 	* ----|- ~ 'E' ~ 'C<x>' ~ 'E' -------ENABLE EEG CHANNEL <1-6>
% 	* ----|-----|-----|-----~ 'D' ------ DISABLE EEG CHANNEL <1-6>
% 	* ----|-----|-----|-----~ 'G<xx>' -- SET EEG CHANNEL <1-6> GAIN <1/2/4/6/8/12/24>
% 	* ----|-----|-----|---- ~ 'I<x>' --- SET EEG INPUT CHANNEL <0-5>
% 	* ----|-----|-------------------------- <0> Electrode
% 	* ----|-----|-------------------------- <1> GND (short)
% 	* ----|-----|-------------------------- <2> BIAS signal
% 	* ----|-----|-------------------------- <3> MVDD (ch3/4:DVDD/4; other:(AVDD+AVSS)/2)
% 	* ----|-----|-------------------------- <4> TEMP sensor
% 	* ----|-----|-------------------------- <5> Test signal
% 	* ----|-----|------------------------------------------------------------------------------
% 	* ----|-----|- ~ 'T' ~ 'A<x>' ------ SET EEG TEST SIGNAL AMPLITUDE <1/2>
% 	* ----|-----|-----|- ~ 'F<x>' ------ SET EEG TEST SIGNAL FRQUENCY <A/B/D>
% 	* ----|-----|-------------------------- <A> 0.98 Hz
% 	* ----|-----|-------------------------- <B> 1.95 Hz
% 	* ----|-----|-------------------------- <D> DC
% 	* ----|-----|------------------------------------------------------------------------------
% 	* ----|-----|- ~ 'S<x>' ------------ SET EEG SAMPLE RATE <1/0>
% 	* ----|-----|-------------------------- <1> 250 Hz
% 	* ----|-----|-------------------------- <0> 500 Hz
% 	* ----|-----|- ~ 'A' ~ 'E' ------------ ENABLE ADDITIONAL CHANNELS (only possible @250Hz!>
% 	* ----|-----|-----|- ~ 'D' ------------ DISBALE ADDITIONAL CHANNELS>
% 	/** ----|-----|- ~ 'S<x>' ------------ SET EEG SAMPLE RATE <1-7>
% 	* ----|-------------------------------- <1> 250 Hz
% 	* ----|-------------------------------- <2> 500 Hz
% 	* ----|-------------------------------- <3> 1 kHz
% 	* ----|-------------------------------- <4> 2 kHz
