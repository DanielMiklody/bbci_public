% 	* -----------------------------------------------------------------------------------------
% 	* -- 'I' ~ 'E' --------------------- ENABLE IMPEDANCE MEASUREMENT
% 	* ----|- ~ 'D' --------------------- DISABLE IMPEDANCE MEASUREMENT
