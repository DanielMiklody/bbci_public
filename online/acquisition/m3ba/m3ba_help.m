% 	* ----|-----|- ~ 'I<xxx>' ---------- SET ACTIVITY INTERRUPT THRESHOLD 62.5 mg/LSB <1-255>
% 	* ----|-----|- ~ 'S<xx>' ----------- SET ACCELEROMETER SAMPLE RATE <1-16>
